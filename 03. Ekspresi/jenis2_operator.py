# jenis2 operator dari ython 
x = 10
y = 5

print(x + y) # penjumlahan
print(x - y) # pngurangan
print(x * y) # perkalian
print(x / y) # pembagian
print(x % y) # modulus
print(x ** y) # eksponen
print(x // y) # pembagian bulat



# Operator Relasional
print(x > y) # lebih besar
print(x < y) # lebih kecil
print(x >= y) # lebih besar sama dengan
print(x <= y) # lebih kecil sama dengan
print(x == y) # sama dengan
print(x != y) # tidak sama dengan


# operator menggunakan string 
x_string = "Dikoding"
y_string = "Indonesia"

print(x_string > y_string) 
print(x_string < y_string) 
print(x_string >= y_string)
print(x_string <= y_string)
print(x_string == y_string)
print(x_string != y_string)


# Operator Logika 
# and digunakan untuk menentukan kondisi yang lebih kompleks
print(True and True)
print(True and False)
print(False and True)
print(False and False)


# Operator not 
# not digunakan untuk negasi bolean 
print(not True)
print(not False)

